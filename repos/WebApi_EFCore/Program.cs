
using Microsoft.EntityFrameworkCore;
using WebApi_EFCore.Data;
using WebApi_EFCore.Repository;

namespace WebApi_EFCore
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.

            builder.Services.AddControllers();
            // Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
            builder.Services.AddEndpointsApiExplorer();


            builder.Services.AddDbContext<ApplicationDBContext>(
                db => db.UseSqlServer(builder.Configuration.GetConnectionString("DBConnStr")));

            builder.Services.AddScoped<IEmployeeServices,EmployeeRepository>();

            builder.Services.AddSwaggerGen();

            var app = builder.Build();

            // Configure the HTTP request pipeline.
            if (app.Environment.IsDevelopment())
            {
                app.UseSwagger();
                app.UseSwaggerUI();
            }

            app.UseHttpsRedirection();

            app.UseAuthorization();


            app.MapControllers();

            app.Run();
        }
    }
}
